    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (listaItens.size() == 0) {
            Alertas.mensagemAviso("Nenhum produto lançado!");
        } else if (tfMemorando.getText().isEmpty()) {
            Alertas.mensagemAviso("Por favor, preencha o campo memorando!");
        } else {
            int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente finalizar a entrada?", "Atenção", 0, 3, null);
            if (opcao == JOptionPane.YES_OPTION) {
                salvarEntrada();
                novaEntrada();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed



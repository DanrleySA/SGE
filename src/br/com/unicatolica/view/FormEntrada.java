    private void addProduto() {
        try {
            if (!tfCodigo.getText().isEmpty()) {
                Integer codigo = Integer.parseInt(tfCodigo.getText());
                if (pDao.buscaProduto(codigo) != null) {
                    p = pDao.buscaProduto(codigo);
                    lbNome.setText(p.getDescricao().toUpperCase());
                    tfValorUnitario.setText(new DecimalFormat("0.00").format(p.getPrecoCusto()));
                    tfValorTotal.setText(new DecimalFormat("0.00").format(p.getPrecoCusto()));
                    tfQuantidade.requestFocus();
                } else {
                    Alertas.mensagemAviso("Produto inexistente!");
                    resetarCampos();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void preencherTabela() {
        DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
        tb.setNumRows(0);
        int numItem = 1;
        for (ProdutoEntrada pe : listaItens) {
            tb.addRow(new Object[]{numItem, pe.getProduto().getCodigo(), pe.getProduto().getDescricao(), pe.getQuantidade(),
                new DecimalFormat("0.00").format(pe.getValorTotal()),
                new DecimalFormat("0.00").format(pe.getValorTotal())});
            numItem++;
        }
    }

    private Double getSubtotal() {
        Double subtotal = 0.0;
        for (ProdutoEntrada pe : listaItens) {
            subtotal += pe.getValorTotal();
        }
        return subtotal;
    }

    private void resetarCampos() {
        lbNome.setText("");
        tfQuantidade.setText("1");
        tfValorUnitario.setText("0,00");
        tfValorTotal.setText("0,00");
        tfCodigo.setText("");
        tfCodigo.requestFocus();
        p = new Produto();
    }

    private void salvarEntrada() {
        eb.getEntrada().setMemorando(tfMemorando.getText());
        eb.getEntrada().setValorTotal(Double.parseDouble(tfSubTotal.getText().replace(",", ".")));
        eb.getEntrada().setData(NossaData.getDataAtual());
        eb.salvar();
        salvarProdutosEntrada();
    }

    private void salvarProdutosEntrada() {
        for (ProdutoEntrada pe : listaItens) {
            peb.setProdutoEntrada(pe);
            peb.salvarProdutoEntrada();
        }
    }

    private void novaEntrada() {
        listaItens.clear();
        peb = new ProdutoEntradaBean();
        eb = new EntradaBean();
        p = new Produto();
        preencherTabela();
        tfMemorando.setText("");
        tfSubTotal.setText("0.00");
    }

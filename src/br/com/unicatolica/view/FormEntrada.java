    private void tfQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfQuantidadeActionPerformed
        if (!tfQuantidade.getText().isEmpty()) {
            int qtde = Integer.parseInt(tfQuantidade.getText());
            if (qtde != 0) {
                p.setQuantidade(qtde);
                tfValorTotal.setText(new DecimalFormat("0.00").format(p.getPrecoCusto() * p.getQuantidade()));
                btAdd.requestFocus();
            }
        }
    }//GEN-LAST:event_tfQuantidadeActionPerformed

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        ProdutoEntrada pe = new ProdutoEntrada();
        pe.setProduto(p);
        pe.setValorUnitario(p.getPrecoCusto());
        pe.setValorTotal(p.getPrecoCusto() * p.getQuantidade());
        pe.setQuantidade(p.getQuantidade());
        listaItens.add(pe);
        preencherTabela();
        tfSubTotal.setText(new DecimalFormat("0.00").format(getSubtotal()));
        resetarCampos();
    }//GEN-LAST:event_btAddActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRowCount() == 1) {
            int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente remover o item?", "Atenção", 0, 3, null);
            if (opcao == JOptionPane.YES_OPTION) {
                int linha = jTable1.getSelectedRow();
                listaItens.remove(linha);
                preencherTabela();
                tfSubTotal.setText(new DecimalFormat("0.00").format(getSubtotal()));
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

